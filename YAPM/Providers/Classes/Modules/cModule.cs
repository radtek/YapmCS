    private static asyncCallbackModuleUnload _sharedcloseM;
    public static int SharedLRUnloadModule(int pid, string name, IntPtr baseAddress)
    {
        if (_sharedcloseM == null)
            _sharedcloseM = new asyncCallbackModuleUnload(new asyncCallbackModuleUnload.HasUnloadedModule(unloadsharedModuleDone), ref _connection);

        System.Threading.WaitCallback t = new System.Threading.WaitCallback(_sharedcloseM.Process());
        int newAction = cGeneralObject.GetActionCount();

        System.Threading.ThreadPool.QueueUserWorkItem(t, new asyncCallbackModuleUnload.poolObj(pid, name, baseAddress, newAction));

        AddSharedPendingTask(newAction, ref t);
    }
    private static void unloadsharedModuleDone(bool Success, int pid, string name, string msg, int actionNumber)
    {
        if (Success == false)
            Misc.ShowError("Could not unload module " + name + " : " + msg);
        RemoveSharedPendingTask(actionNumber);
    }



    // Return opened modules
    public static Dictionary<string, cModule> CurrentLocalModules(int pid)
    {
        Dictionary<string, moduleInfos> _d = Native.Objects.Module.EnumerateModulesByProcessId(pid);
        Dictionary<string, cModule> _res = new Dictionary<string, cModule>();

        foreach (System.Collections.Generic.KeyValuePair<string, moduleInfos> pair in _d)
            _res.Add(pair.Key, new cModule(pair.Value));

        return _res;
    }




