                        _old_Attributes = res;
                    break;
                }

            case "CreateTime":
                {
                    res = this.Infos.CreateTime.ToString();
                    if (res == _old_CreateTime)
                        hasChanged = false;
                    else
                        _old_CreateTime = res;
                    break;
                }

            case "PagedPoolUsage":
                {
                    res = this.Infos.PagedPoolUsage.ToString();
                    if (res == _old_PagedPoolUsage)
                        hasChanged = false;
                    else
                        _old_PagedPoolUsage = res;
                    break;
                }

            case "NonPagedPoolUsage":
                {
