                    if (res == _old_ObjectTypeNumber)
                        hasChanged = false;
                    else
                        _old_ObjectTypeNumber = res;
                    break;
                }
        }

        return hasChanged;
    }




    private static asyncCallbackHandleUnload _sharedcloseH;
    public static int SharedLRCloseHandle(int pid, IntPtr handle)
    {
        if (_sharedcloseH == null)
            _sharedcloseH = new asyncCallbackHandleUnload(new asyncCallbackHandleUnload.HasUnloadedHandle(unloadsharedHandleDone), ref _connection);

        System.Threading.WaitCallback t = new System.Threading.WaitCallback(_sharedcloseH.Process());
        int newAction = cGeneralObject.GetActionCount();

        System.Threading.ThreadPool.QueueUserWorkItem(t, new asyncCallbackHandleUnload.poolObj(pid, handle, newAction));

        AddSharedPendingTask(newAction, ref t);
    }
    private static void unloadsharedHandleDone(bool Success, int pid, IntPtr handle, string msg, int actionNumber)
    {
        if (Success == false)
            Misc.ShowError("Could not unload handle " + handle.ToString() + " : " + msg);
        RemoveSharedPendingTask(actionNumber);
    }
