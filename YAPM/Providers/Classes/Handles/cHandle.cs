    // Retrieve informations by its name
    public override string GetInformation(string info)
    {
        string res = Program.NO_INFO_RETRIEVED;

        if (info == "ObjectCreationDate")
            res = _objectCreationDate.ToLongDateString() + " -- " + _objectCreationDate.ToLongTimeString();
        else if (info == "PendingTaskCount")
            res = PendingTaskCount.ToString();

        switch (info)
        {
            case "Type":
                {
                    res = this.Infos.Type;
                    break;
                }

            case "Name":
                {
                    res = this.Infos.Name;
                    break;
                }

            case "HandleCount":
                {
                    res = this.Infos.HandleCount.ToString();
                    break;
                }

            case "PointerCount":
                {
                    res = this.Infos.PointerCount.ToString();
                    break;
                }

            case "ObjectCount":
                {
                    res = this.Infos.ObjectCount.ToString();
                    break;
                }

            case "Handle":
                {
                    res = this.Infos.Handle.ToString();
                    break;
                }

            case "Process":
                {
                    res = this.Infos.ProcessId.ToString();
                    break;
                }

            case "ObjectAddress":
                {
                    res = "0x" + this.Infos.ObjectAddress.ToString("x");
                    break;
                }

            case "GrantedAccess":
                {
                    res = "0x" + this.Infos.GrantedAccess.ToString("x");
                    break;
                }

            case "Attributes":
                {
                    res = this.Infos.Attributes.ToString();
                    break;
                }

            case "CreateTime":
                {
                    res = this.Infos.CreateTime.ToString();
                    break;
                }

            case "PagedPoolUsage":
                {
                    res = this.Infos.PagedPoolUsage.ToString();
                    break;
                }

            case "NonPagedPoolUsage":
                {
                    res = this.Infos.NonPagedPoolUsage.ToString();
                    break;
                }

            case "ObjectTypeNumber":
                {
                    res = this.Infos.ObjectTypeNumber.ToString();
                    break;
                }
        }

        return res;
    }
