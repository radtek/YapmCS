    public static Dictionary<string, cEnvVariable> CurrentEnvVariables(cProcess process)
    {
        Dictionary<string, cEnvVariable> _dico = new Dictionary<string, cEnvVariable>();

        string[] var = null;
        string[] val = null;
        Native.Objects.EnvVariable.GetEnvironmentVariablesBycProcess(ref process, ref var, ref val);
        var loopTo = var.Length - 1;
        for (int x = 0; x <= loopTo; x++)
        {
            if (_dico.ContainsKey(var[x]) == false)
                _dico.Add(var[x], new cEnvVariable(ref new envVariableInfos(var[x], val[x], process.Infos.ProcessId)));
        }

        return _dico;
    }
