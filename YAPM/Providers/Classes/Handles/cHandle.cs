                    res = "0x" + this.Infos.GrantedAccess.ToString("x");
                    if (res == _old_GrantedAccess)
                        hasChanged = false;
                    else
                        _old_GrantedAccess = res;
                    break;
                }

            case "Attributes":
                {
                    res = this.Infos.Attributes.ToString();
                    if (res == _old_Attributes)
                        hasChanged = false;
